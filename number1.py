"""
This script demonstrates basic number manipulation in Python.
It initializes an integer, converts it to a float, and then back to an integer,
"""
number1 = 10
print("The number is:", number1)
print("The type of number is:", type(number1))
number2 = float(number1)
print("The number converted to float is:", number2)
print("The type of number is:", type(number2))
number3 = int(number2)
print("number converted back to int is:", number3)
print("The type of number is:", type(number3))